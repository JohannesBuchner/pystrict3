print("Calculator")
Firstnumber = eval(input("Enter your first number: "))
Firstoperator = input("Enter you operator: ")
Secondnumber = eval(input ("Enter your second number: "))
if Firstoperator == "+":
    Firstnumber + Secondnumber
    print(Firstnumber + Secondnumber)
if Firstoperator == "*":
    Firstnumber * Secondnumber
    print(Firstnumber * Secondnumber)
if Firstoperator == "/":
    Firstnumber / Secondnumber
    print(Firstnumber / Secondnumber)
if Firstoperator == "-":
    Firstnumber - Secondnumber
    print(Firstnumber - Secondnumber)
